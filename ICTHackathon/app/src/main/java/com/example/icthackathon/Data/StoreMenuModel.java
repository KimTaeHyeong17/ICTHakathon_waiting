package com.example.icthackathon.Data;

public class StoreMenuModel {

    private String serial;
    private String name;
    private String image;
    private String category_kr;
    private String comment;
    private String price;
    private String menu_score;
    private String menu_count;

    public String getMenu_count() {
        return menu_count;
    }

    public void setMenu_count(String menu_count) {
        this.menu_count = menu_count;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getCategory_kr() {
        return category_kr;
    }

    public void setCategory_kr(String category_kr) {
        this.category_kr = category_kr;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getMenu_score() {
        return menu_score;
    }

    public void setMenu_score(String menu_score) {
        this.menu_score = menu_score;
    }
}
